package New_Program;

import java.io.IOException;

public class math {
    public static void main(String[] args) throws IOException {
        int k=10;
        System.out.println(k);
        System.out.println(++k);
        System.out.println(k);
        int l=20;
        System.out.println(l);
        System.out.println(l++);
        System.out.println(l);
        int m=30;
        System.out.println(m);
        System.out.println(--m);
        System.out.println(m);
        int n=40;
        System.out.println(n);
        System.out.println(n--);
        System.out.println(n);
    }
}